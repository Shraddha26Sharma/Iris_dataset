# Import necessary libraries
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn.cluster import KMeans
from sklearn.preprocessing import StandardScaler

# Load the Iris dataset
from sklearn.datasets import load_iris
iris = load_iris()
df = pd.DataFrame(data=iris.data, columns=iris.feature_names)
df['species'] = iris.target

# Perform summary statistics
print(df.describe())

# Visualize the distributions of each feature
for feature in df.columns[:-1]:
    plt.hist(df[feature], bins=10)
    plt.title(feature)
    plt.show()

# Calculate the correlation matrix
corr_matrix = df.corr()
print(corr_matrix)

# Visualize the correlation matrix using a heatmap
plt.imshow(corr_matrix, cmap='coolwarm', interpolation='nearest')
plt.title('Correlation Matrix')
plt.show()

# Identify outliers using scatter plots and box plots
for feature in df.columns[:-1]:
    plt.scatter(df[feature], df['species'])
    plt.title(feature)
    plt.show()

# Perform clustering using k-means
scaler = StandardScaler()
df_scaled = scaler.fit_transform(df.drop('species', axis=1))
kmeans = KMeans(n_clusters=3)
kmeans.fit(df_scaled)
print(kmeans.labels_)

"""The analysis of the Iris dataset reveals that the dataset is well-structured and contains no outliers. The summary statistics and distribution plots provide insights into the characteristics of each feature. The correlation analysis highlights the relationships between the features, and the clustering analysis identifies three distinct clusters corresponding to the three Iris species."""